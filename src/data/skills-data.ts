// Helper functions
export function getAllSkills(): string[] {
  return skillCategories.reduce((allSkills, category) => {
    return [...allSkills, ...category.skills]
  }, [] as string[])
}

export function searchSkills(params: SkillSearchParams): string[] {
  const { query, category, limit = 10 } = params
  
  if (!query.trim()) return []
  
  const searchTerm = query.toLowerCase().trim()
  let skillsToSearch = getAllSkills()
  
  // Filter by category if provided
  if (category) {
    const categoryData = skillCategories.find(cat => 
      cat.name.toLowerCase() === category.toLowerCase()
    )
    if (categoryData) {
      skillsToSearch = categoryData.skills
    }
  }
  
  return skillsToSearch
    .filter(skill => skill.toLowerCase().includes(searchTerm))
    // Sort by relevance: exact matches first, then starts with, then contains
    .sort((a, b) => {
      const aLower = a.toLowerCase()
      const bLower = b.toLowerCase()
      
      // Exact match
      if (aLower === searchTerm) return -1
      if (bLower === searchTerm) return 1
      
      // Starts with
      if (aLower.startsWith(searchTerm) && !bLower.startsWith(searchTerm)) return -1
      if (bLower.startsWith(searchTerm) && !aLower.startsWith(searchTerm)) return 1
      
      // Shorter skills first (more specific)
      return a.length - b.length
    })
    .slice(0, limit)
}

export function getSkillSuggestions(
  selectedSkills: string[] = [], 
  maxSuggestions: number = 8
): SkillSuggestion[] {
  const suggestions = new Set<string>()
  const allSkills = getAllSkills()
  
  // Add related skills based on selected skills
  selectedSkills.forEach(skill => {
    const relatedSkills = skillRelations[skill] || []
    relatedSkills.forEach(relatedSkill => {
      if (!selectedSkills.includes(relatedSkill) && !suggestions.has(relatedSkill)) {
        suggestions.add(relatedSkill)
      }
    })
  })
  
  // Add popular skills if we need more suggestions
  const popularCategories = skillCategories.filter(cat => cat.isPopular)
  popularCategories.forEach(category => {
    category.skills.slice(0, 3).forEach(skill => {
      if (suggestions.size < maxSuggestions && 
          !selectedSkills.includes(skill) && 
          !suggestions.has(skill)) {
        suggestions.add(skill)
      }
    })
  })
  
  // Convert to array and add metadata
  return Array.from(suggestions).slice(0, maxSuggestions).map(skill => {
    const category = skillCategories.find(cat => cat.skills.includes(skill))
    const isRelated = selectedSkills.some(selected => 
      skillRelations[selected]?.includes(skill)
    )
    
    return {
      skill,
      category: category?.name || 'Other',
      relevance: isRelated ? 0.8 : 0.5,
      isRelated
    }
  })
}

export function getSkillsByCategory(categoryName: string): string[] {
  const category = skillCategories.find(cat => 
    cat.name.toLowerCase() === categoryName.toLowerCase()
  )
  return category?.skills || []
}

export function getPopularSkills(limit: number = 20): string[] {
  const popularCategories = skillCategories.filter(cat => cat.isPopular)
  const popularSkills: string[] = []
  
  popularCategories.forEach(category => {
    popularSkills.push(...category.skills.slice(0, 3))
  })
  
  return popularSkills.slice(0, limit)
}

export function getCategoryForSkill(skill: string): string | null {
  const category = skillCategories.find(cat => cat.skills.includes(skill))
  return category?.name || null
}

