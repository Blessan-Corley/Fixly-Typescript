// Helper function to search cities
export function searchCities(query) {
  if (!query) return [];
  
  const searchTerm = query.toLowerCase().trim();
  return indianCities.filter(city => 
    city.name.toLowerCase().includes(searchTerm) ||
    city.state.toLowerCase().includes(searchTerm)
  ).slice(0, 10); // Limit to 10 results for performance
}

// Helper function to get all skills as flat array
export function getAllSkills() {
  return skillCategories.reduce((allSkills, category) => {
    return [...allSkills, ...category.skills];
  }, []);
}

// Helper function to search skills
export function searchSkills(query) {
  if (!query) return [];
  
  const searchTerm = query.toLowerCase().trim();
  const allSkills = getAllSkills();
  
  return allSkills.filter(skill => 
    skill.toLowerCase().includes(searchTerm)
  ).slice(0, 10);
}

